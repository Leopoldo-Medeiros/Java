package ifStatement;

public class If {
    public static void main(String[] args) {

        if(true) {
            System.out.println("This code runs if the condition is true");
        } else {
            System.out.println("This code runs if the condition is false");
        }
        System.out.println("Code outside if statement");
        System.out.println("Hello");
    }
}
